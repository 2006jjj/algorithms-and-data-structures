/*Задача 2.10
На Новом проспекте построили подряд 10 зданий. Каждое здание может быть либо жилым домом, либо магазином, либо офисным зданием.
Но оказалось, что жителям некоторых домов на Новом проспекте слишком далеко приходится идти до ближайшего магазина. 
Для разработки плана развития общественного транспорта на Новом проспекте мэр города попросил вас выяснить, 
какое же наибольшее расстояние приходится преодолевать жителям Нового проспекта, чтобы дойти от своего дома до ближайшего магазина.

Формат ввода
Программа получает на вход десять чисел, разделенных пробелами. Каждое число задает тип здания на Новом проспекте: 
число 1 обозначает жилой дом, число 2 обозначает магазин, число 0 обозначает офисное здание. 
Гарантируется, что на Новом проспекте есть хотя бы один жилой дом и хотя бы один магазин.

Формат вывода
Выведите одно целое число: наибольшее расстояние от дома до ближайшего к нему магазина. 
Расстояние между двумя соседними домами считается равным 1 
(то есть если два дома стоят рядом, то между ними расстояние 1, если между двумя домами есть еще один дом, то расстояние между ними равно 2 и т.д.)

Пример:
Ввод:
2 0 1 1 0 1 0 2 1 2
Вывод:
3*/

#include <iostream> 
using namespace std;


int main()
{setlocale(LC_ALL, ".1251"); std::system("color 0F"); srand(time(NULL));

const int MAX = 10;
int array[MAX];
int test[MAX]{};
int a=-30, a1=30, res;
int count = 0;
int i;
for (int j = 0; j < MAX; j++)
{
	cin >> i;
	array[j] = i;
}
for (int i = 0; i < MAX; i++)
{
	if (array[i] == 2)
		a = i;
	if (array[i] == 1 )
	{
		res = i - a;
		cout << "res=" << res << endl;
		test[count++] = res;
	}
}

cout<<"count=" << count << endl;

for (int j=MAX-1; j>=0; j--)
{
	if (array[j] == 2)
		a1 = j;
	
	if (array[j] == 1 )
	{
		res = a1 - j;
		if (res<test[--count])
			test[count] = res;
		cout << "res1=" << res << endl;
	}
}
res = 0;

for (int k = 0; k < MAX; k++)
{
	if (test[k] > res)
		res = test[k];
	//cout << "res=" << res << endl;
	//cout << "test[" << k+1 << "]=" << test[k] << endl;
}
if(res<MAX)
	cout << res << endl;

cout << endl; system("pause");return 0;
}
